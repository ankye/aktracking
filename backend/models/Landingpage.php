<?php

namespace backend\models;

use Yii;
use backend\models\EntryIndex;
use backend\models\EntryIndexEx;

/**
 * This is the model class for table "{{%mts_landingpage}}".
 *
 * @property integer $id
 * @property string $name
 * @property string $redirectUrl
 * @property integer $clicks
 * @property double $cost
 * @property double $income
 * @property integer $conversion
 * @property integer $active
 */
class Landingpage extends \yii\db\ActiveRecord
{
    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return '{{%mts_landingpage}}';
    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['name', 'redirectUrl'], 'required'],
            [['click','lead'], 'integer'],
            [['cost', 'income'], 'number'],
            [['name', 'redirectUrl'], 'string', 'max' => 255],
        ];
    }

    public static function dropDownItems()
    {
        $items = Yii::$app->cache->get(['landingpageList']);
        if ($items === false) {
            $query = static::find();

            $list = $query->asArray()->all();
            $items = [];
            foreach ($list as $item){
                $items[$item['id']] = $item['name'];
            }

            Yii::$app->cache->set(['landingpageList'], $items, 0);
        }
        return $items;
    }

