    public function getOffers()
    {
        return $this->hasMany(EntryIndex::className(), ['fromID' => 'id'])->onCondition(['type' => EntryIndex::LP_2_OFFER]);
      
    }
    public function getLps()
    {
        return $this->hasMany(EntryIndexEx::className(), ['fromID' => 'id'])->onCondition(['type' =>EntryIndex::LP_2_LP]);

    }

    public function getActiveOffers()
    {
        return $this->hasMany(EntryIndex::className(), ['fromID' => 'id'])->onCondition(['inactive' => 0,'type'=>[EntryIndex::LP_2_OFFER,EntryIndex::LP_2_LP]]);

    }
    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => Yii::t('tracking', 'ID'),
            'name' => Yii::t('tracking', 'Name'),
            'redirectUrl' => Yii::t('tracking', 'Redirect Url'),
            'click' => Yii::t('tracking', 'Clicks'),
            'cost' => Yii::t('tracking', 'Cost'),
            'income' => Yii::t('tracking', 'Income'),
            'lead' => Yii::t('tracking', 'Leads'),
        ];
    }


}
